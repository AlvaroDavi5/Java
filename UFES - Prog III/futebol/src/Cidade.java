
public class Cidade {
	private String nomeCidade;
	private String nomeEstado;

	public String getNomeCidade()
	{
		return nomeCidade;
	}
	public String getNomeEstado()
	{
		return nomeEstado;
	}

	public void setNomeCidade(String nome)
	{
		nomeCidade = nome;
	}
	public void setNomeEstado(String nome)
	{
		nomeEstado = nome;
	}
}

